sub new	{
	my($class, $key) = @_;
    my $self = {};
    bless $self, $class;
    $self->{key} = $key;
    return $self;
}

sub key {
    my($self, $new_key) = @_;
    $self->{key} = $new_key if defined $new_key;
    return $self->{key};
}

sub process_iTMS_key {
    my($self) = @_;
    my $k = '';
    my $key = $self->{key};
    my $siz = length $key;
    for(my $i = 0; $i < $siz; $i++) { 
        $k .= chr $apple_tf_iTMS[$i % 16]->[ord substr($key, $i % 16, 1)];
    }
    return $k;
}

